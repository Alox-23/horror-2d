    def create_tile_dat(self):
        tile_dat = []
        for num, row in enumerate(self.tile_map):
            y = num * TILE_SIZE
            temp_row = []
            for index, tile in enumerate(row):
                x = index * TILE_SIZE
                temp_row.append([(x,y), tile])
            tile_dat.append(sorted(temp_row, key=lambda i: i[1]))
        return tile_dat

    def draw_tiles(self, screan, render_rect):
        t = 0
        for i in self.tiles:
            for j in i:
                if pygame.Rect.colliderect(j.rect, render_rect):
                    j.draw(screan)
                    t += 1
        print(f"Tile objects rendered: {t}")

